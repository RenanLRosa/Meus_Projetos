#+ é adição; - é subtração; * é multiplicação; / é divisão; ** é potênciação; // é divisão inteira; % é o resto; == é o igual
print(('Oi'+'=Olá')*10)
nome = input('Qual é o seu nome? ')
print('Prazer em te conhecer {:20}!'.format(nome))
print('Prazer em te conhecer {:>20}!'.format(nome))
print('Prazer em te conhecer {:<20}!'.format(nome))
print('Prazer em te conhecer {:^20}!'.format(nome))
print('Prazer em te conhecer {:=^20}'.format(nome))